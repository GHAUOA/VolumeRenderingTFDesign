function [intensity_min, intensity_max] = getCellsMinMax(volume)
[I1, I2, I3] = size(volume);
intensity_min = Inf(I1-1, I2-1, I3-1);   %  Min cell volume (I1-1 * I2-1 * I3-1)
intensity_max = -Inf(I1-1, I2-1, I3-1);  %  Max cell volume (I1-1 * I2-1 * I3-1)

vertex_idx = {1:I1-1, 1:I2-1, 1:I3-1; ...
    2:I1, 1:I2-1, 1:I3-1; ...
    2:I1, 2:I2, 1:I3-1; ...
    1:I1-1, 2:I2, 1:I3-1; ...
    1:I1-1, 1:I2-1, 2:I3; ...
    2:I1, 1:I2-1, 2:I3; ...
    2:I1, 2:I2, 2:I3; ...
    1:I1-1, 2:I2, 2:I3 };

for ii=1:8                             % loop thru vertices of all cells
    intensity_min = min(volume(vertex_idx{ii, :}), intensity_min);
    intensity_max = max(volume(vertex_idx{ii, :}), intensity_max);
end
end